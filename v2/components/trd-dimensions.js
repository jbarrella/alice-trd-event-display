function getStackedLayerDimensions() {
    function toDictionary(data, keySelector, useArray= false) {
        const map = new Map();
        for (const d of data) {
            const key = keySelector(d);
            if (!map.has(key))
                map.set(key, useArray ? [d] : d);
            else {
                if (useArray)
                    map.get(key).push(d);
                else console.error("Duplicate key: ", key);
            }
        }

        return map;
    }

    const dimensionData = getDimensions();
    const stackData = toDictionary(dimensionData, d => d.stack, true);
    const stackedLayerData = new Map();
    for (const skey of stackData.keys()) {
        stackedLayerData.set(skey, toDictionary(stackData.get(skey), d => d.layer));
    }

    return stackedLayerData;
}